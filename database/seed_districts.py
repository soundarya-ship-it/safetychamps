# State-level ambulance numbers (some states have custom numbers beyond 108)
STATE_AMBULANCE = {
    "Bihar":          "102",
    "West Bengal":    "102",
    "Uttar Pradesh":  "108",
    "Delhi":          "102",
    "Andaman & Nicobar Islands": "102",
}

def seed_districts(conn):
    """
    Seed the contacts table with government district hospitals and police
    stations for all Indian districts.  Each district generates 2 contacts.
    Skips duplicates (based on name+district match).
    """
    cursor = conn.cursor()
    inserted = 0
    skipped  = 0

    for (district, state, lat, lon) in DISTRICTS:
        ambulance_no = STATE_AMBULANCE.get(state, "108")

        contacts = [
            {
                "name":       f"{district} Government District Hospital",
                "category":   "hospital",
                "phone":      ambulance_no,
                "address":    f"District Hospital, {district}, {state}",
                "lat":        lat,
                "lon":        lon,
                "state":      state,
                "district": district,
                "tier":       2,
                "confidence": 65,
            },
            {
                "name":       f"{district} District Police Control Room",
                "category":   "police",
                "phone":      "112",
                "address":    f"District Police HQ, {district}, {state}",
                "lat":        lat + 0.005,   # slight offset so two pins don't stack
                "lon":        lon + 0.005,
                "state":      state,
                "district": district,
                "tier":       2,
                "confidence": 65,
            },
        ]

        for c in contacts:
            # Avoid duplicate seeding on repeated runs
            exists = cursor.execute(
                "SELECT id FROM contacts WHERE name=? AND district=?",
                (c["name"], c["district"])
            ).fetchone()
            if exists:
                skipped += 1
                continue

            cursor.execute("""
                INSERT INTO contacts
                  (name, category, phone, address, lat, lon,
                   state, district, tier, confidence, is_active,
                   source, user_confirms, user_fails)
                VALUES (?,?,?,?,?,?,?,?,?,?,1,'district_seed',0,0)
            """, (
                c["name"], c["category"], c["phone"], c["address"],
                c["lat"],  c["lon"],      c["state"], c["district"],
                c["tier"], c["confidence"],
            ))
            inserted += 1

    conn.commit()
    print(f"[Districts] Seeded {inserted} contacts ({skipped} already existed) "
          f"across {len(DISTRICTS)} districts")
    return inserted


